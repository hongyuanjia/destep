    parts <- list()
    if (has_min) {
        parts$minimum_schedule <- destep_internal_gain_always_on(
            dest, ep, always_on
        )
    }

    parts$equipment <- eval(as.call(c(
        destep_add, dest, ep,
        lapply(equipment_objects, function(val) bquote("ElectricEquipment" := .(val)))
    )))

    destep_combine_outputs(parts, table = equipment)
}

